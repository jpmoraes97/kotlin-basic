fun main(args: Array<String>) {
    println("###### Tipos Primitivos ######")

    val char = 'M' // Char
    val inteiro = 1230 // Int
    val float = 100F // Float
    val string = "String" // String
    val double = 143.10 // Double
    val long = 12_900_00000000000000L

    println("$char")
    println("$inteiro")
    println("$float")
    println(string)
    println("$double")
    println("$long")
}
