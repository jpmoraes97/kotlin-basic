fun main(args: Array<String>) {
    println("###### Operadores de Atribuição ######")

    /*
     * +=
     * -=
     * /=
     * *=
     */

    var x = 2
    x += 2
    println("$x")

    var y = 10
    y -= 5
    println("$y")

    var z = 200
    z /= 2
    println("$z")

    var xyz = 200
    xyz *= 2
    println("$xyz")
}