fun main(args: Array<String>) {
    println("###### Operadores de Comparação ######")

    /*
     * ==
     * !=
     * >
     * <
     * >=
     * <=
     * is
     */

    val x = 10
    val y = 23

    println(x == y)
    println(x != y)
    println(x > y)
    println(x < y)
    println(x >= y)
    println(x <= y)
}